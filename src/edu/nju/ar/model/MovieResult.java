package edu.nju.ar.model;

public class MovieResult {
	private String name;
	private String type;
	private double score;
	private int last;
	private String cinema_name;
	private String cinema_address;
	private String hall;
	private String date;
	private String start;
	private String end;
	private double noumi_price;
	private double taopiaopiao_price;
	private double yupiaoer_price;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	public int getLast() {
		return last;
	}
	public void setLast(int last) {
		this.last = last;
	}
	public String getCinema_name() {
		return cinema_name;
	}
	public void setCinema_name(String cinema_name) {
		this.cinema_name = cinema_name;
	}
	public String getCinema_address() {
		return cinema_address;
	}
	public void setCinema_address(String cinema_address) {
		this.cinema_address = cinema_address;
	}
	public String getHall() {
		return hall;
	}
	public void setHall(String hall) {
		this.hall = hall;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public double getNoumi_price() {
		return noumi_price;
	}
	public void setNoumi_price(double noumi_price) {
		this.noumi_price = noumi_price;
	}
	public double getTaopiaopiao_price() {
		return taopiaopiao_price;
	}
	public void setTaopiaopiao_price(double taopiaopiao_price) {
		this.taopiaopiao_price = taopiaopiao_price;
	}
	public double getYupiaoer_price() {
		return yupiaoer_price;
	}
	public void setYupiaoer_price(double yupiaoer_price) {
		this.yupiaoer_price = yupiaoer_price;
	}
	
}
