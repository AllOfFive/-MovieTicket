package edu.nju.ar.model;

import java.util.List;

public class UrlVO {

	
	public List<String> movieInfos;
	
	public List<String> cinemaInfos;
	
	public List<String> shopping;

	public List<String> getMovieInfos() {
		return movieInfos;
	}

	public void setMovieInfos(List<String> movieInfos) {
		this.movieInfos = movieInfos;
	}

	public List<String> getCinemaInfos() {
		return cinemaInfos;
	}

	public void setCinemaInfos(List<String> cinemaInfos) {
		this.cinemaInfos = cinemaInfos;
	}

	public List<String> getShopping() {
		return shopping;
	}

	public void setShopping(List<String> shopping) {
		this.shopping = shopping;
	}
	
	
}
