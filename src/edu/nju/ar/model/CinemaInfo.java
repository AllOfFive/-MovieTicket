package edu.nju.ar.model;

public class CinemaInfo {
	private int id;
	private String name;
	private String address;
	private int yupiaoerHas;
	private int taopiaopiaoHas;
	private int baiduHas;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getYupiaoerHas() {
		return yupiaoerHas;
	}
	public void setYupiaoerHas(int yupiaoerHas) {
		this.yupiaoerHas = yupiaoerHas;
	}
	public int getTaopiaopiaoHas() {
		return taopiaopiaoHas;
	}
	public void setTaopiaopiaoHas(int taopiaopiaoHas) {
		this.taopiaopiaoHas = taopiaopiaoHas;
	}
	public int getBaiduHas() {
		return baiduHas;
	}
	public void setBaiduHas(int baiduHas) {
		this.baiduHas = baiduHas;
	}
	
}
